#!/bin/bash
# =============================================================
# نسق — نشر التحديثات على السيرفر
# الاستخدام: bash deploy.sh "وصف التغيير"
# =============================================================
set -e

MSG="${1:-تحديث}"

echo "📦 حفظ التغييرات في git..."
git add -A
git commit -m "$MSG" 2>/dev/null || echo "(لا يوجد تغيير جديد)"

echo "🚀 رفع على السيرفر..."
git push server main

echo "✅ انتهى!"
